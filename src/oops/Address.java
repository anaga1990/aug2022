package oops;

public class Address {
	String crossStreet;
	String houseNumber;
	String area;
	String city;
	String country;
	int zip;
	
	public Address(String crossStreet, String houseNumber, String area, String city, String country, int zip) {
		super();
		this.crossStreet = crossStreet;
		this.houseNumber = houseNumber;
		this.area = area;
		this.city = city;
		this.country = country;
		this.zip = zip;
	}
	
	
	
	
}
