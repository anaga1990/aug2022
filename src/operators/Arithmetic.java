package operators;

public class Arithmetic {
	public static void main(String[] args) {
		int a = 10 + 20;
		System.out.println(a);
		System.out.println("***********************");
		int b = 10 - 5;
		System.out.println(b);
		System.out.println("***********************");
		int c = 103434 * 103344;
		System.out.println(c);
		System.out.println("***********************");
		long d = 20 / 3;
		System.out.println(d);
		System.out.println("***********************");
		int f = 107 % 3;
		System.out.println(f);
	}
}
